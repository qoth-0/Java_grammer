public class C00HelloWorld {
    public static void main(String[] args) {

//      문자열은 쌍따옴표로 감싼다
        System.out.println("hello world1");
        String str = "hello world2";

        int a = 20;
        System.out.println(a);

//      문자열과 숫자를 더하면 문자가 된다
        System.out.println(str + a);
    }
}
